"""CHECK_038 — node_modules listed in .gitignore."""
from __future__ import annotations

from pathlib import Path

from ..result import CheckResult
from . import _is_web_project

CHECK_ID = "CHECK_038"
CHECK_NAME = "node-modules-in-gitignore"
CATEGORY = "stack"
SEVERITY = "error"
DESCRIPTION = ".gitignore lists node_modules so it never gets committed."


def run(repo_path: str) -> CheckResult:
    if not _is_web_project(repo_path):
        return CheckResult(status="warn", message="not a web project — skipped")
    gi = Path(repo_path) / ".gitignore"
    if not gi.is_file():
        return CheckResult(status="fail", message=".gitignore missing")
    content = gi.read_text(errors="replace")
    if any(line.strip() == "node_modules" or line.strip() == "node_modules/"
           for line in content.splitlines()):
        return CheckResult(status="pass", message="node_modules in .gitignore")
    return CheckResult(status="fail",
                       message=".gitignore exists but doesn't include node_modules")
